addString s = do
  Alex $ \st@AlexState{alex_ust=ust} -> case stringBuf ust of
    Just (p, str) ->
      Right (st{alex_ust=ust{stringBuf=Just (p, str ++ s)}}, ())
    _ -> error "impossible"

takeString i@(_, _, _, s) l = do
  addString $ take l s
  skip i l

endString i l = do
  buf <- Alex $ \s@AlexState{alex_ust=ust} -> Right (s, stringBuf ust)
  case buf of
    Just (p, str) -> do
      alexSetStartCode 0
      pure $ (p, StringL str)
    _ -> error "impossible"

beginComment i l = do
  d <- getCommentDepth
  setCommentDepth (d + 1)
  alexSetStartCode comment
  skip i l

endComment i l = do
  d <- getCommentDepth
  setCommentDepth (d - 1)
  when (d == 1) $ alexSetStartCode 0
  skip i l

